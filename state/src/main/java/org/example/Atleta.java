package org.example;

public class Atleta {
    private String nome;
    private AtletaEstado estado;


    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public AtletaEstado getEstado() {
        return estado;
    }

    public void setEstado(AtletaEstado estado) {
        this.estado = estado;
    }
}
